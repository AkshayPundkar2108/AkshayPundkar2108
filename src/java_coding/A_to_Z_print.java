package java_coding;

public class A_to_Z_print {

	public static void main(String[] args) {
		
		for(char i='a'; i<='z'; i++) // for print the capital a to z
		{
		System.out.print("  "+i);
		}
		
		System.out.println("  ");
		for(char i='A'; i<='Z'; i++) // for print the capital A to Z
		{
		System.out.print(" "+i);
		}
		
	System.out.println(" ");
	for(int i=1; i<=50; i++) 	// for print the number
	{
		System.out.print(" "+i);
	}
	
	
}}
